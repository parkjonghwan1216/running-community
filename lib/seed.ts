export function seedGlossaryIfEmpty(db: Database.Database): void {
  const row = db.prepare('SELECT COUNT(*) AS c FROM glossary_terms').get() as { c: number };
  if (row.c > 0) return;
  const insert = db.prepare(
    'INSERT OR IGNORE INTO glossary_terms (term, definition, created_by) VALUES (?, ?, NULL)',
  );
  const tx = db.transaction((rows: Array<[string, string]>) => {
    for (const [term, def] of rows) insert.run(term, def);
  });
  tx(GLOSSARY_SEED);
}
